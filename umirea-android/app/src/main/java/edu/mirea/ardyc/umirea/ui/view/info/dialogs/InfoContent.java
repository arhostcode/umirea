package edu.mirea.ardyc.umirea.ui.view.info.dialogs;

public class InfoContent {

    private String title;
    private String text;

    public InfoContent(String title, String text) {
        this.title = title;
        this.text = text;
    }

    public String getTitle() {
        return title;
    }

    public String getText() {
        return text;
    }
}
